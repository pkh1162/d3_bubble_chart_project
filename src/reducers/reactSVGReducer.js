import { retrieveData, bobData, resources } from '../common_code/tempData'
import {forceCollide, forceY, forceXCombine, simulation, width, height, scaleCircle, changeWidth} from "../common_code/forces";
import * as d3 from 'd3';

const UPDATE_BUBBLE_POSITIONS = 'UPDATE_BUBBLE_POSITIONS';
const BEGIN_SIMULATION = 'BEGIN_SIMULATION';
const GET_SIMULATION = 'GET_SIMULATION';
const BUBBLE_PICKED = 'BUBBLE_PICKED';
const THIS_PICKED = 'THIS_PICKED';
const NEW_SIMULATION = 'NEW_SIMULATION';
const PICK_THIS = 'PICK_THIS';
const PICK_COMPARE_TO = 'PICK_COMPARE_TO';
const UPDATE_TAKE_THIS = 'UPDATE_TAKE_THIS';
const UPDATE_COMPARE_TO = 'UPDATE_COMPARE_TO';

let defaultState = {
    data : resources,
    svg_width : window.screen.availWidth * 0.8,
    bubblePicked : null,
    takeThis : 'Resources',
    compareTo : 'Resources',
    react_simulation : simulation
    .force("forceX", d3.forceX(forceXCombine).strength(0.04))
    .force("forceY", d3.forceY(forceY).strength(0.04))
    .force("collide", d3.forceCollide(forceCollide))
}


console.log("in svg reducer, width is:", window.screen.availWidth, window.screen.width);

const reactSVGReducer = (state = defaultState, action) => {
    switch (action.type) {
        case UPDATE_BUBBLE_POSITIONS :
          //console.log("in reducer");
            return {...state, data : action.data2};
        case BEGIN_SIMULATION :  
            return {...state};
        case NEW_SIMULATION :  
            return {...state};
        case BUBBLE_PICKED : 
          console.log("in bubble reducer", action)
          //console.log(document.getElementById('react_SVG').clientWidth);
          return {...state, bubblePicked : action.bubble}
        case PICK_THIS : 
          //data logic in here
          console.log("takeThis and compareTo are: ", state.takeThis, state.compareTo);
          //retrieveData(action.data, state.takeThis, state.compareTo)
          return {...state, data : retrieveData(action.data, state.takeThis, state.compareTo)}
        case PICK_COMPARE_TO :
          //data logic in here 
          return {...state, data : retrieveData(action.data, state.takeThis, state.compareTo)}    
        case UPDATE_TAKE_THIS : 
          changeWidth();
          console.log("change take this, and take this is: ", state.takeThis);
          return {...state, takeThis : action.data, bubblePicked : null}
        case UPDATE_COMPARE_TO : 
          changeWidth();
          console.log("change compare to, and compare to is: ", state.compareTo);
          return {...state, compareTo : action.data, bubblePicked : null}    
        
        
        default : 
            return state;
    }
}

export default reactSVGReducer;



